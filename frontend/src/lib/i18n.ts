export function formatConfidence(language: Language, confidence: string) {
  if (language === "en") {
    if (confidence === "alta" || confidence === "high") return "HIGH";
    if (confidence === "media" || confidence === "medium") return "MEDIUM";
    return "LOW";
  }
  if (confidence === "high") return "ALTA";
  if (confidence === "medium") return "MEDIA";
  return "BAJA";
}
